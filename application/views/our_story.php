              <h1>Building a top tier iOS talent platform</h1>
              <br>
              <h4 style="color:#3a3a3a; font-weight:400">The concept idea began in September 2022 for a global platform of top tier talent, the idea was to make access to developers fast, easy & all in one singular place. </h4>
			  <br>
              <p>The initial plan began as purely a platform for top tier iOS developers across the world, starting with India as the base of this information. This was due the limitations after getting contracts from multiple MNCs but unable to fulfil quality resources for them over a 4 month time period, project began as the focused approach to solve this problem.</p>
              
              <p>The global pandemic had set the tone for global remote work, this phase was followed by sharp hike salaries for top talent worldwide. The project was an obvious solution to help companies augment resource for their core team regardless of geographic limitations & compensation for top talent was at a global standard. </p>
              
			  <p>The name after much deliberation was between Augemntx & Talrn, the name was finalized with the help of divine intervention as both names were written on two pieces of paper, it was folded, shuffled & one was picked at random with god as witness. The name Talrn represents Talent Right Now, the domain was bought and beta version was launched on 9th October 2022.</p>
              
			  <p>After seeing the huge demand and lack of skilled talent, Talrn focused only on iOS developers, providing reliable resources to companies in need of quickly scale their workforce. We had made sure to provide only top tier iOS developers initially as to limit the scope of fulfillment while providing only the top talent. We began to slowly expand our geographic reach to include other countries such as the US, Europe, Australia, UK & many more over time  offering iOS developers from our platform as a global network.</p>
             
              <p>Talrn began expanding itâ€™s community of developers to over 2000+ top tier iOS developers over the course of the year and became a place for the global iOS talent platform. Our vision is to become the singular marketplace across the world to access and find reliable iOS resources.</p>
            
              <p>The relentless growth for better access to top talent continues to be at the core of everything we do. Today, Talrn connects hundreds of companies with top developers across the globe. Our passion for building technology for augmentation drives all core decisions.</p>
